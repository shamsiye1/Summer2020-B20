package day28_Recap;

public class FrequencyOfWord_Arrays {
    public static void main(String[] args) {

      String[] arr= {"java","c#","java","python"};
      String word= "java";

      int count= 0;
      for(String each:arr){
         if(!word.equalsIgnoreCase(each)){
            continue;
         }
         count++;
      }
        System.out.println(count);











































































































    }











































}
