package day21_Loops;

public class PrintNum_DoWhile {
    public static void main(String[] args) {
        int num = 5;
        do{
            System.out.print(num+ " ");
            num++;
        }while(num<=20);

        System.out.println();
        System.out.print(num);
        System.out.println("===========================================================");


        char ch= 'Z';
        do{
            System.out.print(ch+ " ");
            ch--;
        }while(ch>='A');
































































































    }



































































































}
