package day19_ForLoop;

public class Odd_Even2 {
    public static void main(String[] args) {

     for (int i = 1; i <=50; i++ ){
         if(i%2 ==0){  // if(i%2 !=0) ===> will skip odd number
             continue;
         }
         System.out.print(i + " ");
     }















































































    }






























































}
